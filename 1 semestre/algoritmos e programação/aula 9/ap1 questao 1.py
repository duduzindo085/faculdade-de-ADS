while True:

    print ("digite 2 valores abaixo para verificar se eles são iguais, o primeiro é maior, ou o segundo é maior")

    primeiro = int(input("digite o primeiro numero: "))
    segundo = int(input("digite o segundo numero: "))
    

    if primeiro == segundo:
        print("Esses numeros são iguais")
    
    elif primeiro > segundo:
        print ("o primeiro numero é maior que o segundo")

    elif primeiro < segundo:
        print("o segundo numero é maior que o primeiro")

  

